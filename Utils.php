class Utils {

    function __construct() {
        
    }

    public function array_sort($array, $on, $order = SORT_ASC) {
        if (count($array) > 0) {
            $new_array = array();
            $sortable_array = array();

            foreach ($array as $k => $v) {
                if (is_array($v)) {
                    foreach ($v as $k2 => $v2) {
                        if ($k2 == $on) {
                            $sortable_array[$k] = $v2;
                        }
                    }
                } else {
                    $sortable_array[$k] = $v;
                }
            }

            switch ($order) {
                case SORT_ASC:
                    asort($sortable_array);
                    break;
                case SORT_DESC:
                    arsort($sortable_array);
                    break;
            }

            foreach ($sortable_array as $k => $v) {
                $new_array[$k] = $array[$k];
            }
        } else {
            $new_array = $array;
        }

        return $new_array;
    }

}
